int _tmain(int argc, _TCHAR* argv[])
{
	SArgument arguments;
	if( !(argc > 3) || !ParseArgument( arguments, argv ) )
	{
		Usage();
		return -1;
	}

	if( ProcessExeFileOpen( arguments.m_pExeFileName ) )
		if( ProcessDataFileOpen( arguments.m_pDataFileName ) )
		{
			if( FindSection_N_Injection() )
			{
				SetFilePointer( hExeFILE, 0, NULL, FILE_BEGIN );
				DWORD dwWritten;
				if( WriteFile( hExeFILE, pExeFileBuffer, dwExeFileSize, &dwWritten, NULL ) )
				{
					FlushFileBuffers( hExeFILE );
					CloseHandle( hExeFILE );
					printf( "Complete Injection\n" );
					return 0;
				}
			}
			printf( "Injection Fail\n" );
		}

	return -1;
}

static BOOL FindSection_N_Injection()
{
	DWORD dwIndex = 0;

	DWORD magic = *(DWORD*)CODEINJECTION_STREAM;

	// 일단 한번만 수행하는 것으로 전환하자.
	for( ; dwIndex < dwExeFileSize ; )
	{
		while( (dwIndex < dwExeFileSize) && pExeFileBuffer[dwIndex] != 'W' )
			++dwIndex;

		if( !(dwIndex + sizeof(CODEINJECTION_STREAM) < dwExeFileSize) )
			break;

		if( dwIndex%4 == 0 )
		{
			DWORD cmpValue = *(DWORD*)&pExeFileBuffer[dwIndex];
			if( magic == cmpValue )
			{
				if( memcmp( &pExeFileBuffer[dwIndex], CODEINJECTION_STREAM, sizeof(CODEINJECTION_STREAM) ) == 0 )
				{
					memcpy( &pExeFileBuffer[dwIndex], pDataFileBuffer, sizeof(CODEINJECTION_STREAM) );
					return TRUE;
				}
			}
		}
		++dwIndex;
	}

	return FALSE;
}

static BOOL	ParseArgument( SArgument& rArg, CHAR* argv[] )
{
	CHAR* pExeFileName = argv[1];
	CHAR* pDataFileName = argv[2];
	CHAR* pKey = argv[3];

	if( strlen(pKey) < 3 || strlen(pExeFileName) < 4 || strlen(pDataFileName) < 4 )
	{
		printf( "Check Arguments\n" );
		return FALSE;
	}

	//printf( "%s\t%s\n", pKey, pFileName );

	rArg.m_pExeFileName = pExeFileName;
	rArg.m_pDataFileName = pDataFileName;
	//memcpy( rArg._tKeySTREAM, argv[3], sizeof(rArg._tKeySTREAM) );
	memcpy( CODEINJECTION_STREAM_KEY, argv[3], sizeof(CODEINJECTION_STREAM_KEY) );

	return TRUE;
}


static BOOL ProcessExeFileOpen( CHAR* fileName )
{
	OFSTRUCT pOF;
	hExeFILE = (HANDLE)(__int64)OpenFile( fileName, &pOF, OF_READWRITE );

	if( hExeFILE == INVALID_HANDLE_VALUE )
	{
		printf( "Can't find TargetFile\n" );
		return FALSE;
	}

	dwExeFileSize = GetFileSize( hExeFILE, NULL );

	BYTE* pFileSTREAM = new BYTE[dwExeFileSize];

	DWORD dwReaded = 0;

	if( !ReadFile( hExeFILE, pFileSTREAM, dwExeFileSize, &dwReaded, NULL ) )
	{
		printf( "Can't Read File\n" );
		return FALSE;
	}

	if( dwExeFileSize != dwReaded )
	{
		printf( "Read File Failed\n" );
		return FALSE;
	}

	pExeFileBuffer = pFileSTREAM;

	return TRUE;
}

